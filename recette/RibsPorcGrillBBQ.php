<h1>Ribs de porc grillés au barbecue</h1>

<h2>Ingrédients : 4 personnes</h2>
<ul>
    <li>300 g de ribs de porc</li>
    <h3>Pour la marinade :</h3>
    <li>1 gousse d'ail</li>
    <li> 1 c. à soupe de ketchup</li>
    <li> 1 c. à soupe de cognac </li>
    <li>2,5 c. à soupe de sauce soja légère </li>
    <li>2 c. à café de thym séché </li>
    <li>3 tubercules de gingembre frais râpé </li>
    <li>  1 c. à soupe de miel </li>
    <li>21 c. à café de sucre brun </li>
    <li>1 c. à café de fécule de pomme de terre </li>
</ul>

<h2>Préparation</h2>
<p>
    ÉTAPE 1Pelez et hachez la gousse d'ail.

    ÉTAPE 2Dans un saladier, mélanger l’ail coupé avec le ketchup, le cognac, la sauce soja, le paprika, le miel, le sucre brun, le poivre et la fécule de pomme de terre

    ÉTAPE 3Badigeonnez la viande avec la moitié de la marinade. Réfrigérez la préparation durant 2 h en couvrant le saladier.

    ÉTAPE 4Faites cuire la viande de chaque côté au barbecue durant 25 min. Au bout de ce délai, couvrez de la marinade restante.
</p>

