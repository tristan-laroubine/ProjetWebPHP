<h1> Grillade de poulet au cidre et aux épices</h1>

<h2>Ingrédients : 4 personnes</h2>
<ul>
    <li>4 blancs de poulet</li>
    <li>1 litre de cidre brut</li>
    <li> épices</li>
    <li>herbes aromatiques</li>
    <li> 1 oignon rosé</li>
    <li> 2 tomates</li>
    <li>sel, poivre</li>
</ul>

<h2>Préparation</h2>
<p>
    ÉTAPE 1La veille, ôtez les graisses et dénervez les blancs de poulet sans les abîmer.

    ÉTAPE 2Épluchez, lavez et découpez grossièrement l'oignon.
