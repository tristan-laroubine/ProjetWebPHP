<h1>Travers au porc texan</h1>

<h2>Ingrédients : 4 personnes</h2>
<ul>
    <li>2 travers de porc (40 cm) à coupés en deux </li>
    <li>1/2 verre de ketchup </li>
    <li>1/4 de verre de jus d’ananas non sucré</li>
    <li>2 c. à soupe de sauce Worcester </li>
    <li>1 c. à soupe de miel </li>
    <li>2 c. à café de thym séché </li>
    <li>3 tubercules de gingembre frais râpé </li>
    <li>1 bonne pincée de poivre de cayenne </li>
    <li>2 c. à café de sauce soja </li>
    <li>3 gousses d’ail </li>
    <li>2 c. à soupe d’huile d’olive </li>
</ul>

<h2>Préparation</h2>
<p>
    ÉTAPE 1 Placez les travers dans une cocotte-minute, recouvrez-les d’eau froide, ajoutez une pincée de thym et laurier, portez le tout à ébullition pendant 15 min, sortez aussitôt les travers, afin qu’ils refroidissent. Mélangez les ingrédients de la marinade.

    ÉTAPE 2 Tartinez ensuite la viande de marinade, recouvrez les travers avec le reste de la marinade, mettez le tout à mariner au frigo pour la nuit.

    ÉTAPE 3 Faites-les griller dans une poêle ou au barbecue.
</p>

