    ÉTAPE 7Recouvrez la préparation ensuite, avec 1 ou 2 c. a café d'une ou plusieurs épices choisies selon vos préférences (muscade,curry, piment rouge, paprika...).

    ÉTAPE 8Ensuite, faites de même avec des herbes aromatiques de votre choix (persil, thym, marjolaine, aneth).

    ÉTAPE 9Pour avoir une préparation bien parfumée, vous pouvez utiliser le thym en premier, et ajoutez ensuite une autre herbe de votre choix.

    ÉTAPE 10Renversez tout le cidre sur la préparation.
