<h1>Filet mignon de porc grillé au barbecue</h1>

<h2>Ingrédients : 4 personnes</h2>
<ul>
    <li>1 ou 2 filets mignon de porc de 350 à 400 g chacun</li>
    <li>1 verre de jus d’orange</li>
    <li>1 verre de sauce soja</li>
    <li>1 verre de sauce Worcestershire</li>
</ul>

<h2>Préparation</h2>
<p>
    ÉTAPE 1 Préparez la marinade pour la viande de porc. Dans un petit saladier, mélangez à l’aide d’une cuillère le jus
    d'orange, la sauce soja et la sauce Worcestershire. Versez la marinade dans un plat creux et plongez-y les filets
    mignons de porc.

    ÉTAPE 2 Couvrez de film alimentaire et laissez mariner pendant quelques heures au réfrigérateur.
    PUBLICITÉ
    inRead invented by Teads

    ÉTAPE 3 Allumez le barbecue pour bien le préchauffer. Déposez sur la grille les filets mignons de porc et faites les 
    griller pendant environ une vingtaine de minutes (à adapter selon la chaleur de la braise et l’épaisseur de la
    viande). Retournez les filets toutes les 4 à 5 minutes.

    ÉTAPE 4 Tout au long de la cuisson, mouillez la viande de temps en temps avec la marinade. Servez les filets mignons
    porc grillé au barbecue coupés en petites tranches rondes, épaisses d’un centimètre environ.
</p>

