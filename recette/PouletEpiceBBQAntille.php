<h1>Poulet épicé au barbecue façon antillaise</h1>

<h2>Ingrédients : 4 personnes</h2>
<ul>
    <li>4 cuisses de poulet</li>
    <li>33 cl de limonade sucrée</li>
    <li>2 citrons verts</li>
    <li>1 gousse d'ail</li>
    <li>piment doux en poudre</li>
    <li>paprika en poudre</li>
    <li>ail en poudre</li>
    <li>sel</li>
</ul>

<h2>Préparation</h2>
<p>
    ÉTAPE 1Sur votre planche de cuisine, découpez les citrons verts en tranches.

    ÉTAPE 2Versez la limonade dans un récipient creux.

    ÉTAPE 3Ajoutez ensuite le jus d’un citron vert, 1 c. à café de poudre de piment doux et 1 c. à café d'ail en poudre.

    ÉTAPE 4Ajoutez les tranches de citron vert.

    ÉTAPE 5Salez.

    ÉTAPE 6Plongez les cuisses de poulet dans cette marinade.

    ÉTAPE 7Laissez reposer pendant au moins 3h.

    ÉTAPE 8Saupoudrez-les ensuite légèrement avec 1 c. à café de piment en poudre et 1 c. à soupe de paprika.

    ÉTAPE 9Faites-les cuire sur le barbecue pendant environ 15 min.

    ÉTAPE 10Retournez-les cuisses régulièrement afin d'assurer la cuisson de chaque côté.
</p>

