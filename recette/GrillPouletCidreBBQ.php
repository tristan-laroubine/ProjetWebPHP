    ÉTAPE 11Mélangez bien afin que les morceaux de poulet soient complètement immergés. Mettez le couvercle puis, conservez au frais pendant 24 h.

    ÉTAPE 12Le lendemain, retirez vos blancs de poulet du frigo et égouttez-les.

    ÉTAPE 13Faites-les griller à la poêle dans un filet d'huile d'olive.

    ÉTAPE 14En fin de cuisson, versez-y les oignons ainsi que les quartiers de tomates au dessus.
