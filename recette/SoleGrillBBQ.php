<h1>Sole Grillée</h1>

<h2>Ingrédients : 4 personnes</h2>
<ul>
    <li>1 sole</li>
    <li>persil en branches</li>
    <li>un citron</li>
    <li> sauce béarnaise</li>
    <li>sauce diable</li>
    <li> sauce tartare</li>
    <li>huile d'olive</li>
    <li>beurre maître d'hôtel</li>
    <li>sel</li>
    <li>poivre</li>
</ul>

<h2>Préparation</h2>
<p>
    ÉTAPE 1Nettoyez la sole, retirez la peau noire, raclez la peau du côté blanc.

    ÉTAPE 2Ciselez légèrement les deux côtés de la sole.

    ÉTAPE 3Assaisonnez-la avec du sel et poivre à votre goût.

    ÉTAPE 4Arrosez la sole d'huile d'olive et faites la griller à feu modéré.

    ÉTAPE 5Dressez la sole sur un plat bien chaud et l'entourez de persil en branches.

    ÉTAPE 6Envoyez en même temps un citron divisé en 2 ou 3 parties suivant sa grosseur.

    ÉTAPE 7C'est prêt ! Vous pouvez déguster votre sole grillée sans plus attendre !

    ÉTAPE 8Condiments : la sauce béarnaise, le beurre maître d'hôtel, la sauce diable, la sauce tartare,
    accompagnent très bien les poissons grillés. Vous trouverez leur recette sur le site.
</p>

