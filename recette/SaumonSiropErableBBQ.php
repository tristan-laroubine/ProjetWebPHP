<h1>Saumon au sirop d'érable cuit au barbecue</h1>

<h2>Ingrédients : 4 personnes</h2>
<ul>
    <li>4 pavés de saumon </li>
    <li>4 c. à soupe de sirop d'érable</li>
    <li>sel</li>
</ul>

<h2>Préparation</h2>
<p>
    ÉTAPE 1 Faites chauffer votre barbecue et déposez-y les pavés de saumon "avec la peau en bas" car celle-ci permet à la chair de se maintenir pendant la cuisson.

    ÉTAPE 2 Mettez le sirop d'érable dans un bol ou une coupelle.

    ÉTAPE 3 Au bout de 5 min, retournez les pavés, et enduisez-lez de sirop avec le dos d'une cuillère.

    ÉTAPE 4 Salez à votre convenance.
</p>

