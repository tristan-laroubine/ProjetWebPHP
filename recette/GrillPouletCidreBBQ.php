    ÉTAPE 3Après avoir lavé les tomates, découpez-les également grossièrement.

    ÉTAPE 4Avec un pinceau, recouvrez les blancs de poulet de moutarde et posez-les dans un récipient.

    ÉTAPE 5Recouvrez-les des quartiers de tomates et des découpes d'oignon.

    ÉTAPE 6Assaisonnez avec du sel et du poivre.
